# Here are some of the most commonly used string functions in Python (very useful for beginners and everyday coding) 🐍

# 1. lower()
# Converts all characters in a string to lowercase.

# text = "HELLO World"
# print(text.lower())
# Output:

# hello world
# 2. upper()
# Converts all characters to uppercase.

# text = "hello world"
# print(text.upper())
# Output:

# HELLO WORLD
# 3. strip()
# Removes spaces from the beginning and end of a string.

# text = "   hello   "
# print(text.strip())
# Output:

# hello
# 4. split()
# Splits a string into a list.

# text = "apple banana mango"
# print(text.split())
# Output:

# ['apple', 'banana', 'mango']
# 5. replace()
# Replaces part of a string with another value.

# text = "I like Java"
# print(text.replace("Java", "Python"))
# Output:

# I like Python
# 6. find()
# Returns the position of the first occurrence of a substring.

# text = "hello world"
# print(text.find("world"))
# Output:

# 6
# 7. count()
# Counts how many times a substring appears.

# text = "apple apple banana"
# print(text.count("apple"))
# Output:

# 2
# 8. startswith() and endswith()
# Checks if a string starts or ends with a value.

# text = "python programming"

# print(text.startswith("python"))
# print(text.endswith("programming"))
# Output:

# True
# True
# 9. join()
# Joins elements of a list into a string.

# words = ["Python", "is", "fun"]
# print(" ".join(words))
# Output:

# Python is fun
# 10. capitalize()
# Makes the first letter uppercase.

# text = "python is awesome"
# print(text.capitalize())
# Output:

# Python is awesome
# ✅ Most frequently used in real projects:

# split()

# join()

# replace()

# strip()

# lower() / upper()